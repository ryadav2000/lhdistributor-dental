    <script>
        $(document).ready(function() { // Wait for the document to be ready before executing jQuery code
            $(window).resize(function() {
                if ($(window).width() < 991) {
                    $(".btn-mega").click(function() {
                        $("#mega-menu").toggleClass("close-active");
                    });

                    // Add the function to add/remove class on li click


                    $('.menu li').click(function() { // On click of any li tag
                        if (!$(this).hasClass('drop-list-show')) {
                            $('.menu li').removeClass('drop-list-show'); // Remove class from all li tags
                            $(this).addClass('drop-list-show'); // Add class to the clicked li tag
                        } else {
                            $(this).toggleClass('drop-list-show'); // Toggle class on the clicked li tag
                        }
                    });




                } else {
                    // Remove the class when the window width is greater than or equal to 991
                    $("#mega-menu").removeClass("close-active");

                    // Remove the click event when window width is not less than 991
                    $('.menu li').off('click');
                }
            }).resize(); // Trigger the resize event handler immediately on page load
        });
    </script>